export type ModuleStatus = "completed" | "in-progress" | "locked" | "available";

export interface Module {
  id: string;
  title: string;
  description: string;
  duration: string;
  status: ModuleStatus;
  difficulty: "Iniciante" | "Intermediário" | "Avançado";
  topics: string[];
  commands?: { cmd: string; desc: string }[];
  lab?: { title: string; steps: string[] };
  troubleshooting?: { problem: string; solution: string }[];
  checklist?: string[];
  quiz?: { q: string; a: string[]; correct: number }[];
}

export interface Phase {
  id: string;
  number: number;
  title: string;
  subtitle: string;
  description: string;
  icon: LucideIcon;
  color: string;
  modules: Module[];
}

const mod = (
  id: string, title: string, description: string,
  difficulty: Module["difficulty"] = "Intermediário",
  status: ModuleStatus = "available",
  extras: Partial<Module> = {}
): Module => ({
  id, title, description, difficulty, status,
  duration: extras.duration ?? `${30 + Math.floor(Math.random() * 60)} min`,
  topics: extras.topics ?? [
    "Conceitos fundamentais",
    "Arquitetura e componentes",
    "Boas práticas de produção",
    "Casos reais enterprise",
  ],
  commands: extras.commands,
  lab: extras.lab ?? {
    title: `Lab prático: ${title}`,
    steps: [
      `Provisione um ambiente isolado para ${title}.`,
      `Aplique a configuração base seguindo o checklist.`,
      `Valide o estado e colete métricas.`,
      `Simule uma falha e execute o plano de recuperação.`,
    ],
  },
  troubleshooting: extras.troubleshooting ?? [
    { problem: `Falha comum em ${title}`, solution: "Inspecione logs, verifique permissões e revise a configuração base." },
    { problem: "Performance degradada", solution: "Colete métricas, identifique o gargalo e aplique tuning gradual." },
  ],
  checklist: extras.checklist ?? [
    `Compreendi os fundamentos de ${title}`,
    "Executei o laboratório completo",
    "Resolvi o cenário de troubleshooting",
    "Documentei o aprendizado",
  ],
  quiz: extras.quiz ?? [
    {
      q: `Qual a melhor prática inicial ao trabalhar com ${title}?`,
      a: ["Aplicar em produção direto", "Criar ambiente de staging", "Ignorar logs", "Desabilitar segurança"],
      correct: 1,
    },
  ],
});

